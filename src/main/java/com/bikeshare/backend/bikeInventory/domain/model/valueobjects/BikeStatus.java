/**
 * Enumeration representing the bike status.
 * @summary
 * This enumeration is used to represent the bike status. It is used to indicate the status of the bike.
 * The possible values are AVAILABLE, RESERVED, IN_USE, MAINTENANCE, UNAVAILABLE.
 * @since 1.0
 */
public enum BikeStatus {
    AVAILABLE,
    RESERVED,
    IN_USE,
    MAINTENANCE,
    UNAVAILABLE
}
